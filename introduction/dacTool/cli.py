# -*- coding: utf-8 -*-
"""Command-line interface for :mod:`introduction.dacTool`.

The CLI supports the original DAC simulation workflows and a deploy-safe
``sphinx-skel`` helper used by GitHub Pages / Sphinx documentation builds.
"""
from __future__ import annotations

import argparse
import importlib.util
import os
import sys
from pathlib import Path
from typing import Iterable

# ---------- Import shim so `python cli.py` works with absolute imports ----------
if __package__ in (None, ""):
    # Running as a script: add project root to sys.path and import absolute modules.
    pkg_root = os.path.abspath(os.path.join(os.path.dirname(__file__), "..", ".."))
    if pkg_root not in sys.path:
        sys.path.insert(0, pkg_root)

    from introduction.dacTool.apis import RunRequest
    from introduction.dacTool.app import run as run_app
else:
    from .apis import RunRequest
    from .app import run as run_app


_PACKAGE = "introduction.dacTool"
_PROJECT = "digital-control - introduction.dacTool"
_AUTHOR = "digital-control"
_REQUIRED_MODULES = [
    "introduction.dacTool.cli",
    "introduction.dacTool.apis",
    "introduction.dacTool.app",
    "introduction.dacTool.core",
    "introduction.dacTool.design",
    "introduction.dacTool.io",
    "introduction.dacTool.utils",
]
_OPTIONAL_MODULES = [
    "introduction.dacTool.plotting",
    "introduction.dacTool.gui",
    "introduction.dacTool.gui_core",
    "introduction.dacTool.gui_utils",
]


